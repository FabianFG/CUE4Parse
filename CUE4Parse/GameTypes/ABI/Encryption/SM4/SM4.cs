    public static byte[] GetKey(byte[] key, SM4Mode mode)
    {
        if (key is not { Length: >= 16 })
            throw new ArgumentException("SM4 key must be at least 16 bytes (128 bits).");

        var table = mode switch
        {
            SM4Mode.A => TableA,
            SM4Mode.B => TableB,
            SM4Mode.C => TableC,
            SM4Mode.D => TableD,
            SM4Mode.E => TableE,
            _ => throw new ArgumentOutOfRangeException(nameof(mode), mode, null)
        };

        var res = new byte[16];
        for (int i = 0; i < 16; i++)
        {
            res[i] = table[key[i] & 0x3F];
        }

        return res;
    }

    public static void Decrypt(ref byte[] data, byte[] key, SM4Mode mode, SboxMode sboxMode)
    {
        if (mode is not SM4Mode.None) key = GetKey(key, mode);
        var sm4Key = new SM4DecryptionKeyMode(key, sboxMode);
        var engine = SM4Engines.GetOrAdd(sm4Key, _ => new ABSM4Engine(sm4Key));
        for (var i = 0; i < data.Length; i += 16)
        {
            engine.ProcessBlock(data.AsSpan(i, 16));
        }
    }
}

