    public static byte[] DecryptCsv(string fileName, byte[] data)
    {
        if (data is [0xEF, 0xBB, 0xBF, ..] || data.Length < 3)
            return data;
        if (data is not [0x00, 0x00, 0x00, ..])
            throw new ParserException($"Invalid CSV encryption header for: {fileName}");
        if (!_pathsMapping.TryGetValue(fileName, out var mappedPath))
            mappedPath = fileName;

        Span<byte> iv = stackalloc byte[8];
        Encoding.ASCII.GetBytes(mappedPath.AsSpan(0, Math.Min(8, mappedPath.Length)), iv);

        using var des = DES.Create();
        des.Key = _key;
        var decrypted = des.DecryptCbc(data.AsSpan()[3..], iv, PaddingMode.PKCS7);

        if (decrypted is not [0xEF, 0xBB, 0xBF, ..])
            throw new ParserException($"Invalid decrypted text header: {fileName}");

        return decrypted;
    }
