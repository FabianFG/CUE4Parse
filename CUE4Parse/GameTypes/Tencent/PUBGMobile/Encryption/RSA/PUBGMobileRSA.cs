    // We only need RSA for AES and IV, ignore the rest
    public static byte[] DeriveGlobalPakKey(FArchive Ar)
    {
        if (Ar.Length < PAK_INFO_SIZE + ENCRYPTION_DATA_SIZE)
            throw new ParserException(Ar, "PUBG Mobile pak is too small to contain encryption data");

        Ar.Seek(-(PAK_INFO_SIZE + ENCRYPTION_DATA_AFTER_PREFIX_SIZE), SeekOrigin.End);
        var encryptedKey = Ar.ReadBytes(TencentEncryptionUtils.RSA_SIZE);
        var encryptedIv = Ar.ReadBytes(TencentEncryptionUtils.RSA_SIZE);

        return DeriveKeyData(Ar, encryptedKey, encryptedIv, _globalModulus);
    }

    public static byte[] DeriveLitePakKey(FArchive Ar)
    {
        const int footerPaddingSize = 8;
        var encryptionDataSize = ENCRYPTION_DATA_RSA_BLOCK_COUNT * TencentEncryptionUtils.RSA_SIZE;
        if (Ar.Length < PAK_INFO_SIZE + footerPaddingSize + encryptionDataSize)
            throw new ParserException(Ar, "PUBG Lite pak is too small to contain encryption data");

        Ar.Seek(-(PAK_INFO_SIZE + footerPaddingSize + encryptionDataSize), SeekOrigin.End);
        var encryptedKey = Ar.ReadBytes(TencentEncryptionUtils.RSA_SIZE);
        var encryptedIv = Ar.ReadBytes(TencentEncryptionUtils.RSA_SIZE);

        return DeriveKeyData(Ar, encryptedKey, encryptedIv, _liteModulus);
    }

    private static byte[] DeriveKeyData(FArchive Ar, byte[] encryptedKey, byte[] encryptedIv, byte[] modulus)
    {
        var key = TencentEncryptionUtils.DecryptRsaOaep(encryptedKey, modulus);
        var iv = TencentEncryptionUtils.DecryptRsaOaep(encryptedIv, modulus);
        if (key.Length != 32 || iv.Length < 16)
            throw new ParserException(Ar, $"Invalid derived AES key/IV sizes: {key.Length}/{iv.Length}");

        var result = new byte[KEY_DATA_SIZE];
        key.CopyTo(result, 0);
        iv.AsSpan(0, 16).CopyTo(result.AsSpan(32));
        return result;
    }
