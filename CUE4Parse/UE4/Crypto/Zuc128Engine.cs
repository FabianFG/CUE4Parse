    private readonly uint[] _state = new uint[16];
    private uint _r1;
    private uint _r2;

    private Zuc128Engine(ReadOnlySpan<byte> key, ReadOnlySpan<byte> iv)
    {
        for (var i = 0; i < _state.Length; i++)
            _state[i] = ((uint) key[i] << 23) | ((uint) D[i] << 8) | iv[i];

        for (var i = 0; i < 32; i++)
        {
            BitReorganization(out var x0, out var x1, out var x2, out _);
            LfsrWithInitializationMode(F(x0, x1, x2) >> 1);
        }

        BitReorganization(out var discard0, out var discard1, out var discard2, out _);
        F(discard0, discard1, discard2);
        LfsrWithWorkMode();
    }

    public static uint[] GenerateKeyStream(ReadOnlySpan<byte> key, ReadOnlySpan<byte> iv, int wordCount)
    {
        var zuc = new Zuc128Engine(key, iv);
        var output = new uint[wordCount];
        for (var i = 0; i < output.Length; i++)
        {
            zuc.BitReorganization(out var x0, out var x1, out var x2, out var x3);
            output[i] = zuc.F(x0, x1, x2) ^ x3;
            zuc.LfsrWithWorkMode();
        }

        return output;
    }

    private void BitReorganization(out uint x0, out uint x1, out uint x2, out uint x3)
    {
        x0 = ((_state[15] & 0x7FFF8000) << 1) | (_state[14] & 0xFFFF);
        x1 = ((_state[11] & 0xFFFF) << 16) | (_state[9] >> 15);
        x2 = ((_state[7] & 0xFFFF) << 16) | (_state[5] >> 15);
        x3 = ((_state[2] & 0xFFFF) << 16) | (_state[0] >> 15);
    }

    private uint F(uint x0, uint x1, uint x2)
    {
        var w = (x0 ^ _r1) + _r2;
        var w1 = _r1 + x1;
        var w2 = _r2 ^ x2;
        _r1 = S(L1((w1 << 16) | (w2 >> 16)));
        _r2 = S(L2((w2 << 16) | (w1 >> 16)));
        return w;
    }

    private void LfsrWithInitializationMode(uint u) => ShiftState(AddModulo31(Feedback(), u));
    private void LfsrWithWorkMode() => ShiftState(Feedback());

    private uint Feedback()
    {
        var value = AddModulo31(_state[0], Rotate31(_state[0], 8));
        value = AddModulo31(value, Rotate31(_state[4], 20));
        value = AddModulo31(value, Rotate31(_state[10], 21));
        value = AddModulo31(value, Rotate31(_state[13], 17));
        return AddModulo31(value, Rotate31(_state[15], 15));
    }

    private void ShiftState(uint value)
    {
        if (value == 0) value = 0x7FFFFFFF;
        Array.Copy(_state, 1, _state, 0, _state.Length - 1);
        _state[^1] = value;
    }

    private static uint AddModulo31(uint left, uint right)
    {
        var sum = left + right;
        return (sum & 0x7FFFFFFF) + (sum >> 31);
    }

    private static uint Rotate31(uint value, int count) =>
        ((value << count) | (value >> (31 - count))) & 0x7FFFFFFF;

    private static uint L1(uint value) =>
        value ^ BitOperations.RotateLeft(value, 2) ^ BitOperations.RotateLeft(value, 10) ^
        BitOperations.RotateLeft(value, 18) ^ BitOperations.RotateLeft(value, 24);

    private static uint L2(uint value) =>
        value ^ BitOperations.RotateLeft(value, 8) ^ BitOperations.RotateLeft(value, 14) ^
        BitOperations.RotateLeft(value, 22) ^ BitOperations.RotateLeft(value, 30);

    private static uint S(uint value) =>
        (uint) S0[value >> 24] << 24 |
        (uint) S1[value >> 16 & 0xFF] << 16 |
        (uint) S0[value >> 8 & 0xFF] << 8 |
        S1[value & 0xFF];
}
